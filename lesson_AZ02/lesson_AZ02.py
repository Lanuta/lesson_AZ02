import pandas as pd

# Создаём DataFrame с оценками 10 учеников по 5 предметам
data = {
    "Имя": ["Аня", "Борис", "Вика", "Глеб", "Даша", "Егор", "Жанна", "Зоя", "Игорь", "Кира"],
    "Математика": [4, 5, 3, 5, 4, 3, 4, 5, 2, 4],
    "Русский язык": [5, 4, 3, 4, 5, 3, 5, 4, 3, 4],
    "Английский": [4, 4, 5, 3, 4, 3, 5, 4, 4, 5],
    "История": [3, 4, 3, 5, 5, 4, 4, 3, 4, 4],
    "Биология": [5, 3, 4, 4, 4, 5, 3, 5, 4, 3]
}

df = pd.DataFrame(data)

# Выводим первые строки таблицы
print("Первые строки таблицы:\n")
print(df.head())

# Средняя оценка по каждому предмету
print("\nСредние оценки по предметам:\n")
print(df.drop("Имя", axis=1).mean())

# Медианные оценки по предметам
print("\nМедианные оценки по предметам:\n")
print(df.drop("Имя", axis=1).median())

# Q1 и Q3 для Математики
Q1_math = df["Математика"].quantile(0.25)
Q3_math = df["Математика"].quantile(0.75)
IQR_math = Q3_math - Q1_math

print(f"\nQ1 (25%) по математике: {Q1_math}")
print(f"Q3 (75%) по математике: {Q3_math}")
print(f"IQR по математике: {IQR_math}")

# Стандартное отклонение по предметам
print("\nСтандартное отклонение по предметам:\n")
print(df.drop("Имя", axis=1).std())